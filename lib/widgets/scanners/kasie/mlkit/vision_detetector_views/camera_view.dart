class CameraView extends StatefulWidget {
  const CameraView(
      {super.key,
      required this.customPaint,
      required this.onImage,
      this.onCameraFeedReady,
      this.onDetectorViewModeChanged,
      this.onCameraLensDirectionChanged,
      this.initialCameraLensDirection = CameraLensDirection.back});

  final CustomPaint? customPaint;
  final Function(InputImage inputImage) onImage;
  final VoidCallback? onCameraFeedReady;
  final VoidCallback? onDetectorViewModeChanged;
  final Function(CameraLensDirection direction)? onCameraLensDirectionChanged;
  final CameraLensDirection initialCameraLensDirection;

  @override
  State<CameraView> createState() => _CameraViewState();
}

class _CameraViewState extends State<CameraView> {
  static List<CameraDescription> _cameras = [];
  CameraController? _controller;
  int _cameraIndex = -1;
  double _currentZoomLevel = 1.0;
  double _minAvailableZoom = 1.0;
  double _maxAvailableZoom = 1.0;
  double _minAvailableExposureOffset = 0.0;
  double _maxAvailableExposureOffset = 0.0;
  double _currentExposureOffset = 0.0;
  bool _changingCameraLens = false;

  static const mm = 'üçéüçéüçéüçé CameraView üçé';
  bool cameraPermissionOK = false;

  @override
  void initState() {
    super.initState();
    _requestCameraPermission();
  }

  Future<void> _requestCameraPermission() async {
    var status = await Permission.camera.status;
    if (!status.isGranted) {
      status = await Permission.camera.request();
      if (!status.isGranted) {
        if (mounted) {
          showErrorToast(
              message: 'Camera permission is required', context: context);
        }
        return;
      }
    }
    // Permission granted, proceed with camera usage
    pp('$mm ... camera permission granted ... ${status.name}');
    await _initialize();
    setState(() {
      cameraPermissionOK = true;
    });
  }

  Future _initialize() async {
    pp('$mm initialize ...');
    if (_cameras.isEmpty) {
      _cameras = await availableCameras();
    }
    for (var value in _cameras) {
      pp('$mm camera: ${value.name} lensDirection: ${value.lensDirection} sensorOrientation: ${value.sensorOrientation}');
    }
    for (var i = 0; i < _cameras.length; i++) {
      if (_cameras[i].lensDirection == widget.initialCameraLensDirection) {
        _cameraIndex = i;
        break;
      }
    }
    pp("$mm _cameraIndex: $_cameraIndex, ... initialize CameraController");

    _controller =
        CameraController(_cameras[_cameraIndex], ResolutionPreset.low);
    await _controller!.initialize().then((val) {
      pp("$mm _controller!.initialize().then fired!");
      _controller?.takePicture();
    }).catchError((onError) {
      pp("$mm _controller!.initialize().catchError: $onError");
    }).whenComplete(() {
      pp('$mm whenComplete ...');
    });

    if (_cameraIndex != -1) {
      _startLiveFeed();
    }
    setState(() {});
  }

  @override
  void dispose() {
    _stopLiveFeed();
    super.dispose();
  }

  Widget _liveFeedBody() {
    if (_cameras.isEmpty) return const Center(child: Text('No camera found'));
    if (_controller == null) {
      return const Center(child: Text('Controller not ready'));
    }
    if (_controller?.value.isInitialized == false) {
      return const Center(child: Text('Controller is not initialized'));
    }
    return ColoredBox(
      color: Colors.black,
      child: Stack(
        fit: StackFit.expand,
        children: <Widget>[
          Center(
            child: _changingCameraLens
                ? const Center(
                    child: Text('Changing camera lens'),
                  )
                : CameraPreview(
                    _controller!,
                    child: widget.customPaint,
                  ),
          ),
          _backButton(),
          _switchLiveCameraToggle(),
          _detectionViewModeToggle(),
          // _zoomControl(),
          // _exposureControl(),
        ],
      ),
    );
  }

